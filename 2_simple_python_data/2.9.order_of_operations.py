# 2.9 Order of Operations

print(2 ** 3 ** 2)     # the right-most ** operator gets done first!
print((2 ** 3) ** 2)   # use parentheses to force the order you want!

print("\ndata-9-1: What is the value of the following expression:")
print(16 - 2 * 5 // 3 + 1)  # -10 // 3 = 3... 16 - 3 + 1 = 14

print("\ndata-9-2: What is the value of the following expression:")
print(2 ** 2 ** 3 * 3) # 256 * 3 = 768

